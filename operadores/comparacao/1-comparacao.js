// Igaul (==)
//Retorna verdadeiro caso os operadores sejam iguais

var1 = "3";
var2 = 4;

console.log('Para todas as expressões var1 = "3" e var2 = 4');

// Igual (==)
//Retorna verdadeiro caso os operadores sejam iguais

console.log('\nResultado para 3 == var1:   ', 3 == var1);

//Não igual (!=)
console.log('\nResultado para var2 != 3:   ', var2 != 3);

//Estritamente igual
console.log('\nResultado para 3 === 3:     ', 3 === var1);

//Estritamente igual
console.log('\nResultado para 3 !== 3:     ', 3 !== var1);

//Maior que
console.log('\nResultado para var2 > var1:  ', var2 > var1);

//Maior ou igual
console.log('\nResultado para var2 >= var1: ', var2 >= var1);

//Menor
console.log('\nResultado para var1 < var2:  ', var1 < var2);

//Menor ou igual
console.log('\nResultado para var1 <= var2: ', var1 < var2);




