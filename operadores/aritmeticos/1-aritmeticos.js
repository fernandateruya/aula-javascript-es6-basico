// Modulo (%)
// Operador binario. Retorna o inteiro restante da divisao de dois operadores

console.log('Resultado de 12 % 5: ', 12 % 5); // retorna 2

// incremento (++) e Decremento (--)

x = 10;

console.log('\nResultado ++x para x = 10: ', ++x);
console.log('Resultado x++ para x = 10: ', x++); 

x = 10;

console.log('Resultado --x para x = 10: ', --x);
console.log('Resultado x-- para x = 10: ', x--); 



//Negacao (-) e Adição (+)


console.log('\nResultado para -3: ', -3);          // retorna -3
console.log('Resultado para +3:  ', +"3");         // retorna 3
console.log('Resultado para +true :  ', +true );   // retorna 1
console.log('Resultado para -true : ',  -true );    // retorna -1
console.log('Resultado para +false : ',  +false );  // retorna 0
console.log('Resultado para -false : ',  -false );  // retorna 0

// Operador de exponenciação (**)
console.log('\nResultado para  2 ** 3:   ', 2 ** 3);      // retorna 8
console.log('Resultado para 10 ** -1: ', 10 ** -1);    // retorna 0.1

// Operador de agrupamento ()
console.log('\nResultado para 2 * (3 + 2): ', 2 * (3 + 2)); 

